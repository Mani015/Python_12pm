# Type Casting

x = 10
print(type(x))
print(x)
# 10
# <class 'int'>
# above int to conver a float , while using float
print()
y = float(x)
print(type(y))
print('float:',y)
# <class 'float'>
# float: 10.0


# Int to complex
z  =  complex(y)
print('complex:',z)
print(type(z))
# complex: (10+0j)
# <class 'complex'>



