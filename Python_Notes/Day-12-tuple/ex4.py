# ------------------------------------>SET<-----------------------------------------

# Python also includes a data type for sets. A set is an unordered DATA type

# it is denoted by '{  }'
# Curly braces or the set() function can be used to create sets. Note: to create an empty set you have to use set(), not {}; the latter creates an empty dictionary, a data structure that we discuss in the next section.

s1 = {}
# print(type(s1))
# <class 'dict'>

s2 = {1}
# print(s2)
# print(type(s2))
# {1}
# <class 'set'>


s3 = {'banana','mango','apple',11,22,33,44,55,66,'orange','water melon',12.4,45+8j}
print(s3)
# {33, 66, (45+8j), 'orange', 11, 'mango', 44, 'apple', 12.4, 'water melon', 22, 55, 'banana'}
