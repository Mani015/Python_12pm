# will adding two vales


# extend()	Add the elements of a list (or any iterable), to the end of the current list


# sysntax:listname.extend(value_n)
l3  = ['mangos','banana','apple','orane','pineapple','watermelon']
print(l3)
# ['mangos', 'banana', 'apple', 'orane', 'pineapple', 'watermelon']

l3.extend(['graphes','straberry'])
print(l3)
# ['mangos', 'banana', 'apple', 'orane', 'pineapple', 'watermelon', 'graphes', 'straberry']


l3 .extend(['guava','papaya','dragon','pomogranate'])
print(l3)
# ['mangos', 'banana', 'apple', 'orane', 'pineapple', 'watermelon', 'graphes', 'straberry', 'guava', 'papaya', 'dragon', 'pomogranate']

